#include "printf.h"

int ft_integer(int i)
{
	int len_i;
	
	ft_putnbr_fd(i, 1);
	if (i < 0)
		i *= -1;
	len_i = 1;
	while (i < 10)
	{
		i /= 10;
		len_i++;
	}
	return (len_i);	
}
